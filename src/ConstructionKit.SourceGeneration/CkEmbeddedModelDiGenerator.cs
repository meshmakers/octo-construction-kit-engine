using System.Text;
using Meshmakers.Octo.ConstructionKit.Contracts;

namespace Meshmakers.Octo.ConstructionKit.SourceGeneration;

public class CkEmbeddedModelDiGenerator
{
    /// <summary>
    ///     Returns the singleton instance of <see cref="CkEmbeddedModelGenerator" />
    /// </summary>
    public static readonly CkEmbeddedModelDiGenerator Instance = new();

    public string Generate(string ns, CkModelId ckModelId, bool createCkTypeMap)
    {
        StringBuilder sb = new();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Meshmakers.Octo.ConstructionKit.Contracts.ModelCatalogs;");
        sb.AppendLine("using Meshmakers.Octo.ConstructionKit.Contracts.Services;");
        sb.AppendLine($"using {ns};");
        sb.AppendLine();
        sb.AppendLine("namespace Microsoft.Extensions.DependencyInjection;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine(
            $"/// Extension methods for adding a embedded version of construction kit model {ckModelId.FullName} to the DI container.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine($"public static class ServiceCollection{ckModelId.Name.MakeClassName()}Extensions");
        sb.AppendLine("{");
        sb.AppendLine();
        sb.AppendLine($"   public static IServiceCollection AddCkModel{ckModelId.Name.MakeClassName()}(");
        sb.AppendLine("        this IServiceCollection services)");
        sb.AppendLine("    {");
        sb.AppendLine($"        services.AddTransient<ICkEmbeddedModel, {ckModelId.Name.MakeClassName()}CkEmbeddedModel>();");
        if (createCkTypeMap)
        {
            sb.AppendLine($"        services.AddTransient<ICkClassMap, {ckModelId.Name.MakeClassName()}CkTypeMap>();");
        }
        sb.AppendLine();
        sb.AppendLine("        return services;");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }
}